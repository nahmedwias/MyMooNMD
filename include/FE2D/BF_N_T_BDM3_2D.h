static void N_T_BDM3_2D_Funct(double xi, double eta, double *values)
{
  int nBF = 20; // number of basis functions
  // monomials x-component and y-component
  double mon_x[20]={1, 0, xi, 0, xi*xi, 0, xi*xi*xi, 0,  eta, 0, eta*eta, 0, eta*eta*eta, 0, xi*eta, 0, xi*xi*eta, 0, xi*eta*eta, 0};
  double mon_y[20]={0, 1, 0, xi, 0, xi*xi, 0, xi*xi*xi, 0,  eta, 0, eta*eta, 0, eta*eta*eta, 0, xi*eta, 0, xi*xi*eta, 0, xi*eta*eta};
  
  memset(values, 0.0, 2*nBF*SizeOfDouble); // 2 is the space dimension
  for(int i=0; i<nBF; i++)
  {
    for(int j=0; j<nBF; j++)
    {
      values[i    ] += N_T_BDM3_2D_CM[i+j*nBF]*mon_x[j];
      values[i+nBF] += N_T_BDM3_2D_CM[i+j*nBF]*mon_y[j];
    }
  }
}

